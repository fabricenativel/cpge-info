DROP TABLE IF EXISTS Equipe;
DROP TABLE IF EXISTS Personne;

CREATE TABLE Personne(pid INT PRIMARY KEY, nom VARCHAR(30), prenom VARCHAR (30));
CREATE TABLE Equipe (eid INT PRIMARY KEY,
                     nom VARCHAR (30),
                     capitaine INT REFERENCES Personne(pid),
                     coach INT REFERENCES Personne(pid));
CREATE TABLE Membre (pid INT REFERENCES Personne (pid), eid INT REFERENCES Equipe(eid));
CREATE TABLE Rencontre (dom INT References Equipe(eid),
                        ext INT References Equipe(eid),
                        score_d INT,
                        score_e INT,
                        date VARCHAR(8));


