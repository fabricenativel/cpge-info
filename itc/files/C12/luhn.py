def mult2(c):
    d = 2*c
    if d<10:
        return d
    return 1+(d%10)

def pair_impair(n):
    sn = str(n)
    imp = True
    pairs = []
    impairs = []
    for i in range(len(sn)-1,-1,-1):
        if imp:
            impairs.append(int(sn[i]))
        else:
            pairs.append(int(sn[i]))
        imp = not imp
    return pairs, impairs

def luhn(n):
    pairs, impairs = pair_impair(n)
    mpairs = [mult2(x) for x in pairs]
    s = 0
    for i in range(len(pairs)):
        s += mpairs[i]
    for i in range(len(impairs)):
        s += impairs[i]
    return s % 10 == 0

