def num_en_liste(n):
    if n<10:
        return [n]
    else:
        return num_en_liste(n//10) + [n%10]

def pairs_impairs(lst):
    li = []
    lp = []
    impair = True #Le chiffre le plus Ã  droite est de rang 1 donc impair
    for i in range(len(lst)-1,-1,-1):
        if impair:
            li = [lst[i]] + li
        else:
            lp = [lst[i]] + lp
        impair = not impair
    return lp, li

def traite_pairs(lst):
    for i in range(len(lst)):
        if lst[i]*2<10:
            lst[i] = 2* lst[i]
        else:
            lst[i] = 2*lst[i] - 9

def test_num_carte(num):
    lst = num_en_liste(num)
    lp, li = pairs_impairs(lst)
    traite_pairs(lp)
    s = 0
    for x in lp+li:
        s += x
    return s%10==0

